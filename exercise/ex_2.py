# trojkat

a = 10
b = 20
c = 15
h = 12

obwod = a + b + c
pole = int((h * a) / 2)

print("Obwod trojkata wynosi " + str(obwod) + ", zas pole wynosi " + str(pole) + ".")

# prostokat

obwod = 2*a + 2*b
pole = a*b
print("Obwod prostokata wynosi " + str(obwod) + ", zas pole wynosi " + str(pole) + ".")

# kolo
pi = 3.14
r = 10
obwod = 2*pi*r
pole = pi*r**2
print("Obwod ko≈Ça wynosi " + str(obwod) + ", zas pole wynosi " + str(pole) + ".")

# trapez
d = 16
obwod = a+b+c+d
